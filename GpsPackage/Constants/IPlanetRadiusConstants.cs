    /// <summary>
    ///     Gets the radius of Mars in kilometers.
    /// </summary>
    double RadiusOfMarsInKilometers { get; }

    /// <summary>
    ///     Gets the radius of Jupiter in kilometers.
    /// </summary>
    double RadiusOfJupiterInKilometers { get; }

    /// <summary>
    ///     Gets the radius of Saturn in kilometers.
